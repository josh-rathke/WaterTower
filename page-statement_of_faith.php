        <h1>Statement of Faith</h1>
        <p>We, the Board of Directors, the Leadership Council and the Staff of Youth With A Mission Montana-Lakeside, as those who have accepted and responded to the call of Christ’s Great Commission to, “Go into all the world and preach the gospel to every creature,” offer the following Statement of Faith:</p>
        
        
        <div id="scripture" class="doctrine-container">
            <h5 data-doctrine="scripture"><i class="fa fa-plus-square-o"></i> Doctrine of Scripture</h5>
            <p>We affirm the 66 canonical books of the Old and New Testaments to be the only infallible authority for faith and life in the believer and to the unbelieving world as well.</p>
            
                <div class="additional-description">
                    <p>We affirm that these 66 books are the written Word of God to us.  What the Scripture says, God says. Thus all other documents are merely ancillary and not absolutely  binding on faith and life.</p>
                    <p>We affirm that these 66 books have effectively closed the canon and thus any suggestion of a hypothetical addition to these 66 books is disrespectful both to the  traditions of the church and to the guiding Spirit which assembled this authoritative anthology of God's words.</p>
                    <p>As these words in Scripture are binding: our decision making, both corporately and individually, will be guided, directed and instructed by these documents.  The voice of the Scriptures will be the authoritative voice in our decision making.</p>
                    <p>(2 Timothy 3:16)</p>
                </div>
        </div>
        
        
        
        <div id="god" class="doctrine-container">
            <h5 data-doctrine="god"><i class="fa fa-plus-square-o"></i> The Doctrine of God</h5>
            <p>We affirm that God is Triune; that He eternally exists as one God in three persons; that each member of the Trinity - Father, Son and Holy Spirit - are each fully and equally God; we affirm that each distinct person of the Trinity is God and Lord, co-equal in deity, co-equal in glory, co-equal in majesty; yet not three gods, but one God.</p>
            
                <div class="additional-description">
                    <p>We affirm the full deity and full humanity of the incarnate Son; two natures - divine and human - fully existing and fully functioning in the one Christ; that at no time, either before, during or after His incarnation did our Lord Jesus ever cease to be fully divine; rather he “emptied himself” by adding to his pre-existent divine nature, human nature.</p>
                    <p>We also deny Subordinationism, i.e., that the Son and/or Holy Spirit are eternally inferior in nature/essence/being (ontological subordination) and/or eternally subordinate in function. We affirm the eternal equality in nature/essence/being (ontological) of all three Persons of the Trinity. We affirm that the subordination of the Son was voluntary and temporary - for the time of his incarnate ministry for our salvation; that Scripture gives a double account of the Son - one of his voluntary, temporary functional subordination during the time of his incarnate ministry for our salvation, and one of his eternal divine status and nature.</p>
                    <p>We affirm our belief that this understanding of the Triune God and of the incarnation of our Lord Jesus is the universal, catholic Christian faith as established by the Apostle’s Creed, Nicene Creed and Athanasian Creed. We affirm, with these creeds, that a person cannot be saved without believing this firmly and faithfully.</p>
                    <p>(Matt. 28:19; John 1:1; 5:17-24; 10:33; 14:28; Phil. 2:5-11)</p>
                    <p>We affirm that the Triune God is Infinite, i.e., that He is not limited to, restrained by or subject to anything in all creation. He is far greater than anything else that exists; yet He is personal, i.e., that He interacts with humanity and with creation as a person in a dynamic way.</p>
                    <p>We affirm that the Triune God is Eternal, i.e., that He has no beginning or end; that He does not experience time as a succession of moments; that all of time is present to Him; yet He sees events in time and He interacts with humanity and creation in time.</p>
                    <p>We affirm that the Triune God is unchanging in his being, perfections, and purposes; yet He does act and express emotions; He acts and responds differently to different situations in time.</p>
                    <p>We affirm that the Triune God has perfect, complete and absolute knowledge of all things past, present and future, as well as perfect, complete and absolute knowledge of  all things actual, possible and contingent, in one simple and eternal act; that He is eternally and completely aware of all things past, present and future, of all things actual, possible and contingent; that there is nothing outside, beyond or excluded from his knowledge; that he knows the end from the beginning.</p>
                    <p>We affirm that the Triune God is all-powerful, incapable of being hindered or withstood in all his good and benevolent purposes for all things, seen and unseen, known or unknown; that He is not limited to doing only what He actually does, but that He is able to do infinitely more than what He actually does, should He so choose; that he can do all things not absurd: He cannot lie because He is Truth; He cannot deny himself.</p>
                    <p>We affirm that the Triune God is everywhere fully and entirely present at all times, in all his attributes and glory; that there is no place where he is not fully and entirely present in all his attributes and glory; that he is not limited, bound or constrained by time or space.</p>
                    <p>(Ps. 90:2; Ex. 3:14; Mal. 3:6; Ja. 1:17; Gen. 18: 14; Jer. 32:27; Ps. 115:3; Matt. 19:26; Lu. 1:37;  1 John 3:20; Job 37:16; Heb. 4:13; Isa. 46:9-10; Ps. 139:1-4; 1 Sam. 23:11-13; Gen. 1:1; Ps. 139:7-10; 1 Kings 8:27; Jer. 23:23-24)</p>
                </div>
        </div>
        
        
        <div id='judgement' class="doctrine-container">
            <h5 data-doctrine="judgement"><i class="fa fa-plus-square-o"></i> The Fall of Humanity, Sin and Judgement</h5>
            <p>We affirm that humanity was originally created in the image of God, good, without sin and enjoyed blessed relationship with God the Creator. However, humanity, of their own free choice, rebelled against God, disobeyed God and thus forfeited their state of blessedness and fellowship with God.</p>
            
                <div class="additional-description">
                    <p>We affirm that this rebellion against God caused the corruption of the nature of humanity; such that all humanity, i.e., all descendants of our original parents Adam and Eve, are now born spiritually dead, with natures wholly corrupted by sin and suffering a total loss of righteousness and the original good possessed at their creation. The trespass of Adam brought sin and death into the world and led to condemnation for all humanity. This sinful nature has been passed on to all of Adam’s descendants and is thus the cause of all individual and personal sins. All humanity is therefore under the power of sin, deserving of the wrath of God, and unable and incapable, in and of themselves to merit any favor or salvation from God. In our sinful condition we lack spiritual good before God.</p>
                    <p>We affirm that there will be a great, final judgment of believers and unbelievers; that Christ will be the Judge; that those who not have not trusted Christ for salvation and have not obeyed the gospel will suffer the punishment of eternal destruction and exclusion from the presence of the Lord; that believers - Christians - who have trusted Christ for salvation and have had His righteousness imputed to them in justification will be welcomed into the eternal kingdom of God and will receive the fullness of the inheritance of eternal life.</p>
                    <p>(Gen. 1:26-27, 31; 2:15; 3:6-7; 6:5; Ps. 51:5; Rom. 1:18; 3:9; 5:12, 18; Eph. 2:1-3; Matt. 25:46; John 5:25-29; 1 Pet. 1:3-4; 2 Thess. 1:5-10; Rev. 20:11-15)</p>
                </div>
        </div>
        
        
        
        <div id='grace' class="doctrine-container">
            <h5 data-doctrine="grace"><i class="fa fa-plus-square-o"></i> Grace</h5>
            <p>We affirm that Grace is God's free activity and property to bless people and do things for them that they do not deserve.</p>
            
                <div class="additional-description">
                    <p>For the unbeliever, anything good is a product of God's free grace and blessing.</p>
                    <p>For the believer, God chose us by His grace before time.  Further, his grace brought the preaching of the gospel to us, enabled us to respond and believe in Jesus for salvation. Any Christian success or blessing comes by God's grace and undeserved blessing. Even in failure, hardship and persecution; God's grace picks us up and brings blessing. God, by His grace, will carry us, undeservedly, throughout eternity with Jesus.</p>
                    <p>(Ephesians 2:8-10)</p>
                </div>
        </div>
        
        
        
        <div id='justification' class="doctrine-container">
            <h5 data-doctrine="justification"><i class="fa fa-plus-square-o"></i> Justification</h5>
            <p>We affirm that justification takes place in the context of God’s courtroom; that the atoning sacrifice and resurrection of Jesus Christ is the sole ground of justification, while the believer’s faith is the sole condition of justification; that in justification God imputes, reckons, deposits the righteousness of Christ to the account of the believing sinner, and thus declares the believing sinner to be, “righteous.”</p>
            
                <div class="additional-description">
                    <p>(Romans 3:21-27)</p>
                </div>
        </div>
        
        
        
